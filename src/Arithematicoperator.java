
// Arithematic operators +, -, *, /, %


public class Arithematicoperator {
    public static void main(String[] args){
        int v = 200;
        int w = 20;
        int g= 63;
        int p = 2;
        System.out.println(v + w);  //output is 220
        System.out.println(v - w);  //output is 180
        System.out.println(v * w);  // output is 4000
        System.out.println(v / w);  // output is 10
        System.out.println(g % p);  // output is reminder //here output is 1


    }
}
