public class SwitchExample2 {
    public static void main(String[] args){
        char ch = 'u';
        switch (ch){
            case 'a':
                System.out.println("It is vowel " +ch);
            case 'e':
                System.out.println("It is vowel " +ch);
            case 'i':
                System.out.println("It is vowel " +ch);
            case 'o':
                System.out.println("It is vowel " +ch);
            case 'u':
                System.out.println("It is vowel " +ch);
                break;
            default:
                System.out.println("It is consonant");






        }
    }
}
